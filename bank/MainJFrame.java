    public double power(double base, int power) {

        double ans;
        if (power < 0) {
            return 0;
        } else if (power == 0) {
            return 1;
        } else if (power == 1) {
            return base;
        } else {
            ans = base;
            for (int i = 1; i < power; i++) {
                ans = base * ans;
            }
            return ans;
        }
    }
