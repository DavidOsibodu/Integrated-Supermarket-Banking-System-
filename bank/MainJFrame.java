    public void createAccount(GuiElement EL) throws ParseException {
        if (EL.verifInput()) {
            if ((int) accountTypeCombox.getSelectedIndex() == 0) {
                theCustomer.createBankAccount(new CurrentAccount().create(EL.sortcodeInput.getText(),
                        Integer.parseInt(EL.accNoInput.getText()), Double.parseDouble(EL.balanceInput.getText()),
                        EL.nameOfBankInput.getText(), Double.parseDouble(EL.rateInput.getText()),
                        new SimpleDateFormat("dd/MM/yyyy").parse((EL.lastRDateInput.getText())),
                        new Date(System.currentTimeMillis()), 0), 0);
            } else if ((int) accountTypeCombox.getSelectedIndex() == 1) {
                theCustomer.createBankAccount(new ISAAccount().create(EL.sortcodeInput.getText(),
                        Integer.parseInt(EL.accNoInput.getText()), Double.parseDouble(EL.balanceInput.getText()),
                        EL.nameOfBankInput.getText(), Double.parseDouble(EL.rateInput.getText()),
                        new SimpleDateFormat("dd/MM/yyyy").parse((EL.lastRDateInput.getText())),
                        new Date(System.currentTimeMillis()), 0), 0);
            } else if ((int) accountTypeCombox.getSelectedIndex() == 2) {
                theCustomer.createBankAccount(new SavingAccount().create(EL.sortcodeInput.getText(),
                        Integer.parseInt(EL.accNoInput.getText()), Double.parseDouble(EL.balanceInput.getText()),
                        EL.nameOfBankInput.getText(), Double.parseDouble(EL.rateInput.getText()),
                        new SimpleDateFormat("dd/MM/yyyy").parse(EL.lastRDateInput.getText()),
                        new Date(System.currentTimeMillis()), 0), 1);
            } else if ((int) accountTypeCombox.getSelectedIndex() == 3) {
                theCustomer.createBankAccount(new SavingAccount().create(EL.sortcodeInput.getText(),
                        Integer.parseInt(EL.accNoInput.getText()), Double.parseDouble(EL.balanceInput.getText()),
                        EL.nameOfBankInput.getText(), Double.parseDouble(EL.rateInput.getText()),
                        new SimpleDateFormat("dd/MM/yyyy").parse(EL.lastRDateInput.getText()),
                        new Date(System.currentTimeMillis()), 0), 2);
            }
            JOptionPane.showMessageDialog(null, "Account Successfully Created!");
            accountInfoPanel.setVisible(false);
            accountInfoPanel.revalidate();
        }
    }

