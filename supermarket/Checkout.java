    private void clearBasketInfo() {
        try {
            FileWriter fw = new FileWriter("data/baskets/" + username + ".txt", false);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter writer = new PrintWriter(bw);
            
            writer.print("");
            writer.close();
        } catch (IOException ioe) {
            
        }
    }
    
    public void writeStockToFile() {
        try {
            FileWriter fw = new FileWriter("data/stocks/" + username + ".txt", true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter writer = new PrintWriter(bw);
            String stock = "";
            for (Basket.Product p : basket.getProducts()) {
                stock += p.toString() + "\n";
            }
            writer.print(stock);
            writer.close();
        } catch (Exception e) {
        }
    }
